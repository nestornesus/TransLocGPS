            <h3><a href="#">Sistema de Telemetría</a></h3>
          </div>
          <p align="justify">El sistema TransLoc monitorea en tiempo real la ubicación de sus vehículos a través de un modem GPS referencia Syrus, y a su vez se muestra su posición mediante el uso de la API de Google Maps. Es ideal para que desde su computador o celular conozca con exactitud la localización de autos, camiones y mercancía. </p>
          <p align="justify">Muestra la posición actual del vehículo en el mapa. Además, muestra la ruta generada durante la sesión mediante el trazo de una línea continua. </p>
          <p align="justify">Finalmente, permite la consulta de intervalos de tiempo específicos. Solo es cuestión de llenar los parámetros de búsqueda y listo. </p>
